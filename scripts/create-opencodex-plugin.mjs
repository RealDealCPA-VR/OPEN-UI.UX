#!/usr/bin/env node
import { existsSync, mkdirSync, writeFileSync } from 'node:fs';
import { join, resolve } from 'node:path';
import { argv, exit, stdin, stdout } from 'node:process';
import { createInterface } from 'node:readline/promises';

function parseFlags(args) {
  const flags = {
    name: null,
    tool: null,
    runner: null,
    panel: null,
    yes: false,
  };
  for (let i = 0; i < args.length; i++) {
    const a = args[i];
    if (a === '--name' && args[i + 1]) {
      flags.name = args[++i];
    } else if (a === '--tool' && args[i + 1]) {
      flags.tool = args[++i];
    } else if (a === '--runner' && args[i + 1]) {
      flags.runner = args[++i];
    } else if (a === '--panel' && args[i + 1]) {
      flags.panel = args[++i];
    } else if (a === '-y' || a === '--yes') {
      flags.yes = true;
    } else if (a === '--help' || a === '-h') {
      printHelp();
      exit(0);
    } else if (!a.startsWith('-') && !flags.name) {
      flags.name = a;
    }
  }
  return flags;
}

function printHelp() {
  stdout.write(
    [
      'Usage: pnpm create opencodex-plugin <name> [options]',
      '',
      'Options:',
      '  --name <name>     Plugin name (kebab-case)',
      '  --tool <id>       Tool contribution id (default: ${name}_tool)',
      '  --runner <id>     Runner contribution id (default: ${name}-runner)',
      '  --panel <id>      Panel contribution id (default: ${name}-panel)',
      '  -y, --yes         Accept defaults non-interactively',
      '',
    ].join('\n'),
  );
}

async function ask(rl, question, fallback) {
  const answer = await rl.question(`${question}${fallback ? ` (${fallback})` : ''}: `);
  const trimmed = answer.trim();
  return trimmed.length > 0 ? trimmed : fallback;
}

function validateName(name) {
  if (!name) return 'name is required';
  if (!/^[a-z][a-z0-9-]*$/.test(name))
    return 'name must be kebab-case (lowercase letters, digits, dashes)';
  return null;
}

function toIdentifier(name) {
  return name.replace(/-([a-z])/g, (_, c) => c.toUpperCase());
}

async function main() {
  const args = argv.slice(2);
  const flags = parseFlags(args);
  let name = flags.name;
  let toolId = flags.tool;
  let runnerId = flags.runner;
  let panelId = flags.panel;

  if (!flags.yes && (!name || !toolId || !runnerId || !panelId)) {
    const rl = createInterface({ input: stdin, output: stdout });
    try {
      if (!name) name = await ask(rl, 'Plugin name (kebab-case)', 'my-plugin');
      const err = validateName(name);
      if (err) {
        stdout.write(`Error: ${err}\n`);
        exit(1);
      }
      if (!toolId) toolId = await ask(rl, 'Tool id', `${name.replace(/-/g, '_')}_tool`);
      if (!runnerId) runnerId = await ask(rl, 'Runner id', `${name}-runner`);
      if (!panelId) panelId = await ask(rl, 'Panel id', `${name}-panel`);
    } finally {
      rl.close();
    }
  } else {
    if (!name) name = 'my-plugin';
    const err = validateName(name);
    if (err) {
      stdout.write(`Error: ${err}\n`);
      exit(1);
    }
    if (!toolId) toolId = `${name.replace(/-/g, '_')}_tool`;
    if (!runnerId) runnerId = `${name}-runner`;
    if (!panelId) panelId = `${name}-panel`;
  }

  const targetDir = resolve(process.cwd(), name);
  if (existsSync(targetDir)) {
    stdout.write(`Error: directory ${targetDir} already exists\n`);
    exit(1);
  }
  mkdirSync(targetDir);
  mkdirSync(join(targetDir, 'src'));

  const camel = toIdentifier(name);
  const pascal = camel.length > 0 ? camel[0].toUpperCase() + camel.slice(1) : camel;
  const displayName = name
    .split('-')
    .map((p) => (p.length > 0 ? p[0].toUpperCase() + p.slice(1) : p))
    .join(' ');

  const manifest = {
    name,
    version: '0.1.0',
    displayName,
    description: `${displayName} — generated by create-opencodex-plugin`,
    license: 'MIT',
    entry: 'dist/index.js',
    engines: { opencodex: '^0.1.0' },
    permissions: [],
    contributions: {
      tools: [toolId],
      runners: [{ id: runnerId, displayName: `${displayName} Runner` }],
      panels: [{ id: panelId, title: displayName, entry: 'panel.html' }],
    },
  };

  writeFileSync(join(targetDir, 'opencodex.plugin.json'), JSON.stringify(manifest, null, 2) + '\n');

  const insideMonorepo = existsSync(resolve(process.cwd(), 'pnpm-workspace.yaml'));
  const opencodexDepRange = insideMonorepo ? 'workspace:*' : '*';

  const pkg = {
    name: `@opencodex-plugin/${name}`,
    version: '0.1.0',
    license: 'MIT',
    type: 'module',
    main: 'dist/index.js',
    scripts: {
      build: 'tsc -p tsconfig.json',
      test: 'vitest run',
      clean: 'rimraf dist',
    },
    dependencies: {
      '@opencodex/core': opencodexDepRange,
      '@opencodex/plugin-sdk': opencodexDepRange,
      zod: '^3.22.0',
    },
    devDependencies: {
      typescript: '^5.4.0',
      vitest: '^1.4.0',
    },
  };
  writeFileSync(join(targetDir, 'package.json'), JSON.stringify(pkg, null, 2) + '\n');

  const tsconfig = {
    compilerOptions: {
      target: 'ES2022',
      module: 'ESNext',
      moduleResolution: 'bundler',
      outDir: 'dist',
      rootDir: 'src',
      strict: true,
      esModuleInterop: true,
      declaration: true,
    },
    include: ['src'],
  };
  writeFileSync(join(targetDir, 'tsconfig.json'), JSON.stringify(tsconfig, null, 2) + '\n');

  const vitestConfig = `import { defineConfig } from 'vitest/config';

export default defineConfig({
  test: {
    globals: false,
    environment: 'node',
    include: ['src/**/*.{test,spec}.ts'],
  },
});
`;
  writeFileSync(join(targetDir, 'vitest.config.ts'), vitestConfig);

  const indexTs = `import { z } from 'zod';
import { definePlugin } from '@opencodex/plugin-sdk';
import {
  defineTool,
  type ChatEvent,
  type SubagentRunOptions,
  type SubagentRunner,
} from '@opencodex/core';

const ${camel}Input = z.object({
  message: z.string().optional(),
});

const ${camel}Tool = defineTool({
  name: '${toolId}',
  description: '${displayName} — generated tool',
  inputZod: ${camel}Input,
  permissionTier: 'read',
  async execute({ message }) {
    return \`${displayName}: \${message ?? 'hello'}\`;
  },
});

async function* run${pascal}(opts: SubagentRunOptions): AsyncIterable<ChatEvent> {
  if (opts.signal?.aborted) {
    yield { type: 'done', stopReason: 'cancelled' };
    return;
  }
  yield { type: 'text_delta', delta: \`stub runner received: \${opts.task}\` };
  yield { type: 'usage', inputTokens: 0, outputTokens: 0 };
  yield { type: 'done', stopReason: 'end_turn' };
}

const ${camel}Runner: SubagentRunner = {
  id: '${runnerId}',
  displayName: '${displayName} Runner',
  streaming: true,
  run: run${pascal},
};

export default definePlugin({
  activate(host) {
    host.registerTool(${camel}Tool);
    host.registerRunner(${camel}Runner);
    host.logger.info('${name} plugin activated');
  },
});
`;
  writeFileSync(join(targetDir, 'src', 'index.ts'), indexTs);

  const testTs = `import { describe, expect, it } from 'vitest';

describe('${name}', () => {
  it('placeholder smoke test', () => {
    expect(true).toBe(true);
  });
});
`;
  writeFileSync(join(targetDir, 'src', 'index.test.ts'), testTs);

  const panelHtml = `<!doctype html>
<html lang="en">
  <head>
    <meta charset="utf-8" />
    <meta http-equiv="Content-Security-Policy" content="default-src 'none'; style-src 'unsafe-inline'; img-src data:; script-src 'none'; connect-src 'none'; frame-ancestors 'self';" />
    <title>${displayName}</title>
    <style>
      body { font-family: system-ui, sans-serif; padding: 1rem; }
      h1 { font-size: 1.1rem; margin: 0 0 0.5rem; }
    </style>
  </head>
  <body>
    <h1>${displayName}</h1>
    <p>This panel is loaded as a sandboxed iframe by OpenCodex.</p>
  </body>
</html>
`;
  writeFileSync(join(targetDir, 'panel.html'), panelHtml);

  const readme = `# ${displayName}

A starter OpenCodex plugin scaffolded by \`pnpm create opencodex-plugin\`.

## Build

\`\`\`
pnpm install
pnpm build
\`\`\`

## Test

\`\`\`
pnpm test
\`\`\`

## Install in OpenCodex

In the OpenCodex Plugins panel, choose **Sideload from folder…** and select this directory.

## Permissions

The scaffold ships with \`permissions: []\` — the minimum needed for the host
to accept the manifest. Each capability requires explicit consent from the
user, and OpenCodex refuses to register contributions the manifest does not
claim. Add only the permissions you actually use:

| Permission        | Required to                                            |
| ----------------- | ------------------------------------------------------ |
| \`workspace.read\`  | Register tools with \`permissionTier: 'read'\`           |
| \`workspace.write\` | Register tools with \`permissionTier: 'write'\`          |
| \`shell.execute\`   | Register tools with \`permissionTier: 'execute'\`        |
| \`network.fetch\`   | Register tools with \`permissionTier: 'network'\`        |
| \`agent.runner\`    | Register a \`SubagentRunner\` contribution              |
| \`ui.panel\`        | Surface a contributed UI panel (e.g. \`panel.html\`)     |
| \`settings.read\`   | Call \`host.getSetting(key)\`                            |
| \`settings.write\`  | Call \`host.setSetting(key, value)\`                     |

The generated entry registers a \`read\` tool plus a runner, so you will
typically want \`["workspace.read", "agent.runner"]\` — and \`"ui.panel"\` if
you keep the panel contribution.

## UI panel trust model

\`panel.html\` is loaded inside a renderer iframe. The scaffold ships a strict
CSP \`<meta>\` tag (no script-src, no remote network) so a freshly created
plugin cannot exfiltrate data even before the host-side sandboxing lands —
relax it deliberately when you add JavaScript.
`;
  writeFileSync(join(targetDir, 'README.md'), readme);

  stdout.write(`\nCreated ${name} at ${targetDir}\n`);
  stdout.write('Next steps:\n');
  stdout.write(`  cd ${name}\n`);
  stdout.write('  pnpm install\n');
  stdout.write('  pnpm build\n');
}

main().catch((err) => {
  stdout.write(`Error: ${err instanceof Error ? err.message : String(err)}\n`);
  exit(1);
});
